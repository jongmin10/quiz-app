/**
 * 배열에서 n개를 무작위 추출
 * @param {any[]} arr
 * @param {number} n
 */
function pickRandom(arr, n) {
  const shuffled = [...arr].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, n);
}

/**
 * 카테고리에서 하(4)·중(4)·상(2) 문제를 무작위 추출
 * @param {string} category
 * @returns {Array}
 */
export function getLocalQuiz(category) {
  const bank = BANK[category];
  if (!bank) throw new Error(`알 수 없는 카테고리: ${category}`);

  return [
    ...pickRandom(bank['하'], 4),
    ...pickRandom(bank['중'], 4),
    ...pickRandom(bank['상'], 2),
  ];
}
